function drawBox(int $width, int $length, int $reihe, int $spalte) {

    echo "\033[{$reihe};{$spalte}Hв”Ң";

    $oben = $width + $spalte;
    $oberesI = $spalte + 1;

    while ($oberesI < $oben) {

        echo"\033[{$reihe};{$oberesI}Hв”Җ";
        $oberesI++;

    }

    echo "\033[{$reihe};{$oberesI}Hв”җ";

    $rechts = $length + $reihe;
    $rechtesI = $reihe + 1;

    while ($rechtesI < $rechts) {

        echo"\033[{$rechtesI};{$oberesI}Hв”Ӯ";
        $rechtesI++;

    }

    echo"\033[{$rechtesI};{$oberesI}Hв”ҳ";

    $unteresI = $oben - 1;

    while ($unteresI > $spalte) {

        echo "\033[{$rechtesI};{$unteresI}Hв”Җ";
        $unteresI--;

    }

    $linkesI = $rechtesI;

    while ($linkesI > $reihe) {

        echo"\033[{$linkesI};{$spalte}Hв”Ӯ";
        $linkesI--;

    }

    echo"\033[{$rechtesI};{$spalte}Hв””";

}
function drawAsciiArt(int $zeile, int $spalte, string $filePath): void {

    if (!file_exists($filePath)) {
        echo "Fehler: Datei '$filePath' nicht gefunden.\n";
        return;
    }

    $ascii = file_get_contents($filePath);
    $lines = explode("\n", $ascii);

    foreach ($lines as $i => $line) {

        $row = $zeile + $i;
        echo "\033[" . $row . ";" . $spalte . "H" . $line;

    }
}
